    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        

        try (Connection conn = DatabaseConnection.getKoneksi()) {
            String sql = "INSERT INTO barang (kode_barang, nama_barang, kategori, satuan, harga_beli, harga_jual, stok) "
                    + "VALUES (?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, jTextField3.getText());
            pstmt.setString(2, jTextField3.getText());
            pstmt.setString(3, jComboBox1.getSelectedItem().toString());
            pstmt.setString(4, jComboBox1.getSelectedItem().toString());
            pstmt.setDouble(5, Double.parseDouble(jTextField1.getText()));
            pstmt.setDouble(6, Double.parseDouble(jTextField6.getText()));
            pstmt.setInt(7, Integer.parseInt(jTextField7.getText()));

            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
            tampilkanStokBarang();
          
        } catch (SQLException | NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

