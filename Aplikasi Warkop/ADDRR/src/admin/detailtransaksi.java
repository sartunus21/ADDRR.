    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
    String pesanan = jTextField1.getText();
    String jumlah = jTextField3.getText();
    String subtotal = jTextField4.getText();
    String catatan = jTextField6.getText();
    
    // Validasi sederhana, pastikan field tidak kosong
    if (pesanan.isEmpty() || jumlah.isEmpty() || subtotal.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Data pesanan tidak boleh kosong!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    // Ambil model dari tabel Anda
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    
    // Tambahkan data sebagai baris baru
    model.addRow(new Object[]{pesanan, jumlah, subtotal, catatan}); // Sesuaikan urutan dengan kolom tabel Anda
    
    // Kosongkan kembali field input untuk item berikutnya
    clearItemFields();
    
    // Update total keseluruhan jika ada
    updateTotal();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
    
    // Pastikan ada baris yang dipilih
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih item di tabel yang ingin diubah.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    // Ambil data terbaru dari field input
    String pesanan = jTextField1.getText();
    String jumlah = jTextField3.getText();
    String subtotal = jTextField4.getText();
    String catatan = jTextField6.getText();

    // Ambil model dari tabel
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    
    // Update nilai pada baris yang dipilih
    model.setValueAt(pesanan, selectedRow, 0); // Kolom 0
    model.setValueAt(jumlah, selectedRow, 1); // Kolom 1
    model.setValueAt(subtotal, selectedRow, 2); // Kolom 2
    model.setValueAt(catatan, selectedRow, 3); // Kolom 3

    JOptionPane.showMessageDialog(this, "Item berhasil diubah.");
    clearItemFields();
    updateTotal();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
    
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih item di tabel yang ingin dihapus.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    // Konfirmasi sebelum menghapus
    int confirm = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus item ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
    
    if (confirm == JOptionPane.YES_OPTION) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.removeRow(selectedRow); // Hapus baris dari tabel
        
        JOptionPane.showMessageDialog(this, "Item berhasil dihapus.");
        updateTotal();
    }
    }//GEN-LAST:event_jButton3ActionPerformed

