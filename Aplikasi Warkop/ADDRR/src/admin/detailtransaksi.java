    private void updateTotal() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    double totalKeseluruhan = 0;

    // Loop melalui setiap baris di tabel
    for (int i = 0; i < model.getRowCount(); i++) {
        try {
            Object value = model.getValueAt(i, 2);
           
            double subtotal = Double.parseDouble(model.getValueAt(i, 2).toString());
            totalKeseluruhan += subtotal;
        } catch (Exception e) {
            // Tangani jika ada nilai yang tidak valid di kolom subtotal
            System.err.println("Nilai tidak valid di tabel pada baris " + i + ": " + e.getMessage());
        }
    }

    // Tampilkan hasil total di label (sesuaikan nama 'lblTotalKeseluruhan')
    // Anda bisa format sesuai keinginan, contoh: "Rp 150000.0"
    jTextField4.setText("Rp " + totalKeseluruhan);
    }
    
